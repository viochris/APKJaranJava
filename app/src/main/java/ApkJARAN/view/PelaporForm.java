    private void kirimBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kirimBtnActionPerformed
        String tipePelapor = pelapor.getSelectedItem().toString();
        String almt = alamat.getText();
        String kebutuhanKhusus = kebutuhan.getSelectedItem().toString();
        String jumlahKKStr = totalKK.getText();
        String telp = noTelp.getText();

        if (tipePelapor.equals("Pilih...") || almt.isEmpty() || kebutuhanKhusus.equals("Pilih...") || jumlahKKStr.isEmpty() || telp.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi semua kolom dulu ya");
            return;
        }

        if (!jumlahKKStr.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Jumlah KK harus angka");
            return;
        }

        else if (!telp.matches("\\d+") || !telp.startsWith("62") || !telp.matches("^62\\d{8,12}$")) {
            JOptionPane.showMessageDialog(null, "Nomor telepon Harus Berupa Angka, dimulai dengan 62, dan berjumlah 10-14 angka", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int confirm = JOptionPane.showConfirmDialog(this, "Apakah Kamu Sudah Yakin Dengan Laporanmu?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                controller.kirimPelapor(tipePelapor, almt, kebutuhanKhusus, Integer.parseInt(jumlahKKStr), telp, Session.loggedInUsername);
                JOptionPane.showMessageDialog(this, "Data berhasil dikirim");

                // reset form
                pelapor.setSelectedIndex(0);
                alamat.setText("");
                kebutuhan.setSelectedIndex(0);
                totalKK.setText("");
                noTelp.setText("");

                tampilkanDataKeTabel();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal kirim data: " + e.getMessage());
        }
    }//GEN-LAST:event_kirimBtnActionPerformed

