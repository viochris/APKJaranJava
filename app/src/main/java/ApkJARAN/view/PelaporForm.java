    public void tampilkanDataKeTabel() {
        try {
            ArrayList<String[]> data = controller.getDataPelapor(Session.loggedInUsername);
            DefaultTableModel model = new DefaultTableModel(new String[]{
                "Pelapor", "Alamat", "Kebutuhan Khusus", "Jumlah KK", "No. Telp", "Status", "Tanggal", "ID"
            }, 0);


            for (String[] row : data) {
                model.addRow(row);
            }

            Table.setModel(model);
            Table.getColumnModel().getColumn(7).setMinWidth(0);
            Table.getColumnModel().getColumn(7).setMaxWidth(0);
            Table.getColumnModel().getColumn(7).setWidth(0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error tampilkan data: " + e.getMessage());
        }
    }

    
