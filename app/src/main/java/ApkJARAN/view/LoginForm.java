    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        String username = user.getText();
        String password = new String(pswd.getPassword());
                
        if(username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan password wajib diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            Login user = loginController.handleLogin(username, password);
            if (user != null) {
                for (Login u : loginHistory) {
                    if (u.getUsername().equalsIgnoreCase(username)) {
                        JOptionPane.showMessageDialog(this, "User ini sudah login sebelumnya", "Info", 
                                JOptionPane.INFORMATION_MESSAGE);
                        return;
                    }
                }

                // Tambahkan ke riwayat login
                loginHistory.add(user);
                
                Session.loggedInUsername = username;
                String posisi = user.getPosisi();

                if (posisi.equalsIgnoreCase("Admin")) {
                    new InputForm().setVisible(true);
                } else if (posisi.equalsIgnoreCase("Member")) {
                    new PelaporForm().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Posisi tidak valid, hubungi admin ya", "Error", 
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Username atau password salah", "Login Gagal", 
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

